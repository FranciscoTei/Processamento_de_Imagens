
# combination.py

def combine_values(value1, value2):
    """
    Combines two values by adding them.

    Parameters:
    value1 (numeric): The first value.
    value2 (numeric): The second value.

    Returns:
    numeric: The sum of value1 and value2.
    """
    return value1 + value2

def subtract_values(value1, value2):
    """
    Subtracts the second value from the first value.

    Parameters:
    value1 (numeric): The value to be subtracted from.
    value2 (numeric): The value to subtract.

    Returns:
    numeric: The result of subtracting value2 from value1.
    """
    return value1 - value2
